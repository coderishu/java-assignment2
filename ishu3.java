class ishu3
{
	public static void main(String args[])
	{
		for(int i=1;i<=5;i++)
		{
			for(int j=1;j<=i;j++)
			{
				System.out.print(j);
			}
			System.out.println("\n");
		}
		for(int k=1;k<=4;k++)
		{
			for(int l=1;l<=4;l++)
			{
				if(l<k)
				{
					System.out.print(" ");
				}
				else{
					System.out.print(l);
				}
			}
			System.out.println("\n");
		}
	}
}
